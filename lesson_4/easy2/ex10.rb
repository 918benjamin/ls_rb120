What are the benefits of using Object Oriented Programming in Ruby?
Think of as many as you can.

- Code reuse
- Code organization (makes it easier to find things and group them together)
- Simplicity (custom classes and definitions make things easy)
- Ease of reading code
- Clarity
- Encapsulation - hide functionality away and only expose what is needed publicly

LS solution:
- Creating objects allows programmers to think more abstractly about the code they are writing
- Objects are represented by nouns so are easier to conceptualize
- Allows us to only expose functionality to the parts of code that need it
  namespace issues are much harder to come across
- Allows us to easily give functionality to different parts of an application without duplication
- We can build applications faster as we can reuse pre-written code
- As the software becomes more complex, the complexity can be more easily managed

